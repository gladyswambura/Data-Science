# SELECT: Used to retrieve data from a database table. You can specify which columns you want to fetch and apply conditions to filter the rows you need.

# INSERT: Allows you to add new data (rows) into a table.

# UPDATE: Used to modify existing data in a table. You can specify which rows to update and what changes to make.

# DELETE: Allows you to remove data (rows) from a table. Similar to UPDATE, you can specify conditions to delete specific rows.

# CREATE TABLE: Used to create a new table in the database. You define the table's structure by specifying column names and data types.

# ALTER TABLE: Enables you to modify an existing table's structure. You can add, modify, or delete columns.

# DROP TABLE: Deletes an entire table and its data from the database.

# SQL Clauses and Keywords:

# WHERE Clause: Allows you to filter rows based on specific conditions. It is used with SELECT, UPDATE, and DELETE statements to narrow down the rows affected.

# ORDER BY: Used to sort the result set of a query in ascending or descending order based on one or more columns. This helps in organizing data for analysis.

# GROUP BY: Empowers you to group rows based on the values in one or more columns. It is typically used with aggregate functions like SUM, COUNT, AVG, etc., to summarize data within each group.